//creo función de ejecución(Click boton)
function muestra_oculta(contenido)
{
//declaro función y paso parámetro selector de referencia
let div = document.getElementById(contenido);
//uso condicional id para saber si el div está oculto o visible
if (div.style.display == "none")
    {
        div.style.display = "flex";
    }
else
    {
        div.style.display = "none";
    }
}

